    /**
     * Load d·ªØ li·ªáu l√™n table
     */
    private void loadData() {
        tableModel.setRowCount(0);
        try {
            List<Hotel> hotels = hotelService.getAllHotels();
            
            for (Hotel h : hotels) {
                Object[] row = {
                    h.getHotelId(),
                    h.getHotelName(),
                    h.getPhone(),
                    h.getAddress()
                };
                tableModel.addRow(row);
            }
            
            System.out.println("‚úÖ ƒê√£ load " + hotels.size() + " kh√°ch s·∫°n");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "‚ùå L·ªói khi load d·ªØ li·ªáu: " + e.getMessage(), 
                "L·ªói", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    /**
     * ƒêi·ªÅn th√¥ng tin t·ª´ table v√†o form
     */
    private void dienFormTuBang() {
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            try {
                txtMaks.setText(jTable1.getValueAt(row, 0).toString());
                txtTenks.setText(jTable1.getValueAt(row, 1).toString());
                txtSdt.setText(jTable1.getValueAt(row, 2).toString());
                txtDiachi.setText(jTable1.getValueAt(row, 3).toString());
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    
    /**
     * T√¨m ki·∫øm kh√°ch s·∫°n
     */
    private void btnTimkiemActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String keyword = txtTimkiem.getText().trim();
            
            if (keyword.isEmpty()) {
                loadData();
                return;
            }
            
            tableModel.setRowCount(0);
            List<Hotel> allHotels = hotelService.getAllHotels();
            int count = 0;
            
            for (Hotel h : allHotels) {
                if (h.getHotelId().toLowerCase().contains(keyword.toLowerCase()) ||
                    h.getHotelName().toLowerCase().contains(keyword.toLowerCase()) ||
                    h.getPhone().contains(keyword) ||
                    h.getAddress().toLowerCase().contains(keyword.toLowerCase())) {
                    
                    Object[] row = {
                        h.getHotelId(),
                        h.getHotelName(),
                        h.getPhone(),
                        h.getAddress()
                    };
                    tableModel.addRow(row);
                    count++;
                }
            }
            
            if (count == 0) {
                JOptionPane.showMessageDialog(this, 
                    "‚ùå Kh√¥ng t√¨m th·∫•y kh√°ch s·∫°n v·ªõi t·ª´ kh√≥a: " + keyword, 
                    "K·∫øt qu·∫£", 
                    JOptionPane.INFORMATION_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "‚ùå L·ªói: " + e.getMessage(), 
                "L·ªói", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
