    private boolean kiemTraForm(boolean isAdd) {
        if (txtMaKH.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Vui l√≤ng nh·∫≠p m√£ kh√°ch h√†ng!");
            txtMaKH.requestFocus();
            return false;
        }

        if (txtHoten.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Vui l√≤ng nh·∫≠p h·ªç t√™n!");
            txtHoten.requestFocus();
            return false;
        }

        if (txtSdt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Vui l√≤ng nh·∫≠p s·ªë ƒëi·ªán tho·∫°i!");
            txtSdt.requestFocus();
            return false;
        }

        // Ch·ªâ check username/password khi th√™m m·ªõi HO·∫∂C accountType != GUEST
        String accountType = cboAccountType.getSelectedItem().toString();
        if (isAdd && !"GUEST".equals(accountType)) {
            if (txtUsername.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Vui l√≤ng nh·∫≠p username!");
                txtUsername.requestFocus();
                return false;
            }

            if (new String(txtPassword.getPassword()).trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Vui l√≤ng nh·∫≠p password!");
                txtPassword.requestFocus();
                return false;
            }
        }

        return true;
    }

