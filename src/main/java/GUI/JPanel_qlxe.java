    private void khoiTaoDuLieu() {
        taiDuLieu();

        tblDanhSachXe.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                dienFormTuBang();
            }
        });
    }

    private void taiDuLieu() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachXe.getModel();
        model.setRowCount(0);

        try {
            List<Vehicle> danhSachXe = vehicleService.getAllVehicles();

            for (Vehicle xe : danhSachXe) {
                model.addRow(new Object[]{
                    xe.getVehicleId(),
                    xe.getLicensePlate(),
                    xe.getVehicleType(),
                    xe.getCapacity(),
                    xe.getStatus()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói khi t·∫£i d·ªØ li·ªáu: " + e.getMessage());
        }
    }

    private void themXe() {
        if (!kiemTraForm()) {
            return;
        }

        try {
            Vehicle xe = new Vehicle();
            xe.setVehicleId(txtMaXe.getText().trim());
            xe.setLicensePlate(txtBienSo.getText().trim());
            xe.setVehicleType(txtLoaiXe.getText().trim());
            xe.setCapacity(Integer.parseInt(txtSucChua.getText().trim()));
            xe.setStatus(cboTrangThai.getSelectedItem().toString());
            xe.setCreatedAt(new Date());

            if (vehicleService.addVehicle(xe) != null) {
                JOptionPane.showMessageDialog(this, "Th√™m xe th√†nh c√¥ng!");
                lamMoiForm();
                taiDuLieu();
            } else {
                JOptionPane.showMessageDialog(this, "M√£ xe ƒë√£ t·ªìn t·∫°i!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "S·ª©c ch·ª©a ph·∫£i l√† s·ªë!", "L·ªói", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void suaXe() {
        if (txtMaXe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn xe c·∫ßn s·ª≠a!", "C·∫£nh b√°o", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!kiemTraForm()) {
            return;
        }

        try {
            Vehicle xe = vehicleService.findVehicleById(txtMaXe.getText().trim());
            if (xe == null) {
                JOptionPane.showMessageDialog(this, "Kh√¥ng t√¨m th·∫•y xe!", "L·ªói", JOptionPane.ERROR_MESSAGE);
                return;
            }

            xe.setLicensePlate(txtBienSo.getText().trim());
            xe.setVehicleType(txtLoaiXe.getText().trim());
            xe.setCapacity(Integer.parseInt(txtSucChua.getText().trim()));
            xe.setStatus(cboTrangThai.getSelectedItem().toString());

            if (vehicleService.updateVehicle(xe.getVehicleId(), xe)) {
                JOptionPane.showMessageDialog(this, "C·∫≠p nh·∫≠t th√†nh c√¥ng!");
                lamMoiForm();
                taiDuLieu();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "S·ª©c ch·ª©a ph·∫£i l√† s·ªë!", "L·ªói", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void xoaXe() {
        if (txtMaXe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn xe c·∫ßn x√≥a!", "C·∫£nh b√°o", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int xacNhan = JOptionPane.showConfirmDialog(this, "B·∫°n c√≥ ch·∫Øc mu·ªën x√≥a xe n√†y?", "X√°c nh·∫≠n", JOptionPane.YES_NO_OPTION);
        if (xacNhan == JOptionPane.YES_OPTION) {
            try {
                if (vehicleService.deleteVehicle(txtMaXe.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "X√≥a th√†nh c√¥ng!");
                    lamMoiForm();
                    taiDuLieu();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "L·ªói: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void timKiemXe() {
        String tuKhoa = txtTimKiem.getText().trim();
        if (tuKhoa.isEmpty()) {
            taiDuLieu();
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblDanhSachXe.getModel();
        model.setRowCount(0);

        try {
            List<Vehicle> danhSachXe = vehicleService.searchVehicles(tuKhoa);
            for (Vehicle xe : danhSachXe) {
                model.addRow(new Object[]{
                    xe.getVehicleId(),
                    xe.getLicensePlate(),
                    xe.getVehicleType(),
                    xe.getCapacity(),
                    xe.getStatus()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói t√¨m ki·∫øm: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void dienFormTuBang() {
        int dongDuocChon = tblDanhSachXe.getSelectedRow();
        if (dongDuocChon >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblDanhSachXe.getModel();

            txtMaXe.setText(model.getValueAt(dongDuocChon, 0).toString());
            txtBienSo.setText(model.getValueAt(dongDuocChon, 1).toString());
            txtLoaiXe.setText(model.getValueAt(dongDuocChon, 2).toString());
            txtSucChua.setText(model.getValueAt(dongDuocChon, 3).toString());
            cboTrangThai.setSelectedItem(model.getValueAt(dongDuocChon, 4).toString());
        }
    }

    private void lamMoiForm() {
        txtMaXe.setText("");
        txtBienSo.setText("");
        txtLoaiXe.setText("");
        txtSucChua.setText("");
        cboTrangThai.setSelectedIndex(0);
        txtTimKiem.setText("");
        tblDanhSachXe.clearSelection();
    }

    private boolean kiemTraForm() {
        if (txtMaXe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p m√£ xe!");
            return false;
        }
        if (txtBienSo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p bi·ªÉn s·ªë!");
            return false;
        }
        if (txtLoaiXe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p lo·∫°i xe!");
            return false;
        }
        if (txtSucChua.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p s·ª©c ch·ª©a!");
            return false;
        }
        return true;
    }
